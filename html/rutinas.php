    <h1 class="titulo">Rutinas</h1>

    <p class="infotitulo">¿Que son las Rutinas?<br></br>Las rutinas sirven para mejorar la salud física y mental, aumentar la fuerza, la resistencia y la flexibilidad, y contribuir a un estilo de vida activo y equilibrado.</br>La elección de la rutina depende de los objetivos individuales, ya sea mejorar la salud general, ganar músculo, perder peso o simplemente mantenerse en forma.</p>


    <h4 class="famosas">Algunas rutinas famosas</h4>


    <h1 class="heavy">Heavy Duty</h1>
    <p class="infoheavy">La rutina Heavy Duty de Mike Mentzer es un enfoque de entrenamiento diseñado para la eficiencia y la máxima estimulación muscular en un corto período de tiempo.</br>Desarrollada por el culturista y entrenador Mike Mentzer, esta rutina se basa en principios de alta intensidad y volumen bajo.</p>
    <h5 class="heavy2">Caracteristicas</h5>
  
    <p class="infoheavy2">Intensidad Extrema: La rutina se centra en ejercicios intensos y de alta intensidad con el objetivo de estimular al máximo las fibras musculares. Cada serie se realiza hasta el fallo muscular, lo que significa que se levanta el peso hasta que no se puede completar otra repetición con buena forma.</br>
        <br>
        Frecuencia Baja: Se realiza un bajo volumen de entrenamiento, con menos frecuencia en comparación con otras rutinas. Los entrenamientos pueden ser menos frecuentes, permitiendo un mayor tiempo de recuperación entre sesiones.</br>
        <br>
        Entrenamiento con Peso Pesado: La rutina prioriza el uso de pesos más pesados, realizando menos repeticiones pero con mayor carga. Esto se hace para maximizar la estimulación de las fibras musculares.</br>
        <br>
        Corto Tiempo de Entrenamiento: Cada sesión de entrenamiento es breve pero intensa, evitando sesiones prolongadas en el gimnasio. Se enfoca en la calidad del esfuerzo en lugar de la cantidad de tiempo invertido.</br>
        <br>
        Principio de Descanso Completo: Se enfatiza la importancia del descanso completo entre las sesiones de entrenamiento para permitir una recuperación adecuada y evitar el sobreentrenamiento.</br>
        <br>
        Principio de Progresión: Se busca progresar constantemente, ya sea aumentando el peso, la intensidad o el número de repeticiones, para asegurar una adaptación continua y un crecimiento muscular.</br>
        <br>
        Selección de Ejercicios Clave: La rutina se enfoca en ejercicios compuestos clave que trabajan grandes grupos musculares, minimizando el número total de ejercicios.</br>
        <br>
        Individualización: Se destaca la importancia de adaptar la rutina a las necesidades y capacidad individual de cada persona, reconociendo que no todos los cuerpos responden de la misma manera al entrenamiento.</p>

